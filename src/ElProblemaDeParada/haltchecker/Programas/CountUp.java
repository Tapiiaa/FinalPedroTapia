package ElProblemaDeParada.haltchecker.Programas;
/**
 * Clase que cuenta indefinidamente hasta arriba
 * @author Pedro Alonso Tapia Lobo
 */
public class CountUp {
    //Clase que cuenta indefinidamente hasta arriba
    public void countUp() {
        int i = 0;
        while (true) {
            System.out.println(i);
            i++;
        }
    }


}
